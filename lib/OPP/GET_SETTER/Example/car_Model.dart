class CarModel {
  String? _color;
  String? _type;
  double? _speed;


  String? get color => this._color;

  set color(String? color) => this._color = color;

  String? get type => this._type;

  set type(String? type) => this._type = type;

  double? get speed => this._speed;

  set speed(double? speed) => this._speed = speed;
}
